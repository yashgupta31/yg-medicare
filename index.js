var cartData= JSON.parse(localStorage.getItem('cart'))|| [];
newLaunch(data.newLaunches)
function newLaunch(Data){
    Data.forEach((elem) => {
        let newLaunchContainer = document.getElementById('new-launch-scoll-container');

        let newLaunchElement = document.createElement('div');
        newLaunchElement.className= 'each-new-launch'
    
        let launchImg= document.createElement('img');
        launchImg.src= elem.img;
        launchImg.className= 'each-new-launch-img'
    
        let launchName= document.createElement('p');
        launchName.className= 'each-new-launch-name'
        launchName.innerText= elem.name;

        let LaunchPriceDiscount= document.createElement('div');
        LaunchPriceDiscount.className= 'each-new-launc-price-and-discount';
        LaunchPriceDiscount.innerHTML= `<span>₹${elem.newPrice}</span> <span style="color: #F47779;">(${elem.discount}%)</span>`

        let button= document.createElement('div');
        button.innerText= 'Add to cart';
        button.className='each-new-launch-btn';

        newLaunchElement.append(launchImg, launchName, LaunchPriceDiscount, button)
        newLaunchContainer.append(newLaunchElement)
    
    
        for(let i=0; i<cartData.length; i++){
            if(elem.id==cartData[i].id){
                button.innerText= 'Added'
                button.style.opacity= '60%'
                // break;
            }
            
        }
    
        
        newLaunchElement.querySelector('.each-new-launch-btn').addEventListener('click', (e) => {
            e.preventDefault();
            let loginUser= JSON.parse(localStorage.getItem('login'))|| [];

            if(loginUser.length==1){
                let isAdded=false;
            for(let j=0; j<cartData.length; j++){
                if(cartData[j].id==elem.id){
                    isAdded=true;
                }
                
            }

            if(!isAdded){
                cartData.push(elem)
                localStorage.setItem('cart', JSON.stringify(cartData))
                button.innerText= 'Added'
                button.style.opacity= '60%'
                alert('Item added in cart')
            }else{
                button.innerText= 'Added'
                button.style.opacity= '60%'
                alert('Item already in cart')
            }
            }else{
                 alert('Please signup')
                 setTimeout(()=>{
                    window.location.href= './signup.html'
                 }, 2000)
                
            }
            document.querySelector('.nav-cart-counter').innerText= cartData.length;
        });
    });
}

document.querySelector('.nav-cart-counter').innerText= cartData.length;


